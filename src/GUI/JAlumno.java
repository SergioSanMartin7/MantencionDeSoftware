    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Metodos met = new Metodos();
        String rut = this.txtRutC.getText().trim();
        Alumno cl = met.buscarCliente(rut);
        if(this.txtRutC.getText().trim().equals(cl.getRut())){
        this.txtNombreC.setText(cl.getNombre().toString());
        this.txtDireccionC.setText(cl.getDireccion().toString());
        this.txtEdadC.setText(String.valueOf(cl.getEdad()));
        this.txtComunaC.setText(cl.getComuna().toString());
        }
        else {
            JOptionPane.showMessageDialog(this, "Este Rut No Existe", "Error de Busqueda", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

