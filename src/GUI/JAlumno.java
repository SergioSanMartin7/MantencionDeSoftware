    private void insertarDatos() {
        
               Alumno cli = new Alumno();
               Metodos met = new Metodos();
               cli.setRut(this.txtRutC.getText().trim());
               cli.setNombre(this.txtNombreC.getText().trim());
               cli.setEdad(Integer.parseInt(this.txtEdadC.getText().trim()));
               cli.setDireccion(this.txtDireccionC.getText().trim());
               cli.setComuna(this.txtComunaC.getText().trim());
               
               met.ingresarCliente(cli);
               
               Usuario user = new Usuario("","","","","",0,"");
               
               user.setRut(this.txtRutC.getText().trim());
               user.setContraseña(this.pswCliente.getText().trim());
               
               
               met.ingresarUsuario(user);
    }
    
    private void limpiarCampos(){
        txtRutC.setText("");
        txtNombreC.setText("");
        txtEdadC.setText("");
        txtDireccionC.setText("");
        txtComunaC.setText("");
        pswCliente.setText("");
    }
    
    private boolean existeCliente() {
        boolean siExiste = false;
        
        Metodos met = new Metodos();
        Alumno cl = met.buscarCliente(this.txtRutC.getText().trim());
        if(this.txtRutC.getText().trim().equals(cl.getRut())){
            siExiste = true;
        }
        else{
            siExiste = false;
        }
        return siExiste;
    }
