    private void btnRegistrarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarBActionPerformed
        // TODO add your handling code here:
        if(this.txtRut.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this,"Ingrese el codigo ","Error",JOptionPane.ERROR_MESSAGE);
            this.txtRut.requestFocus();
            return;
        }

        if(this.txtNombreB.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this,"Ingrese su nombre ","Error",JOptionPane.ERROR_MESSAGE);
            this.txtNombreB.requestFocus();
            return;
        }

        if(this.txtDireccionB.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this,"Ingrese la direccion ","Error",JOptionPane.ERROR_MESSAGE);
            this.txtDireccionB.requestFocus();
            return;
        }

        if(this.txtComunaB.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this,"Ingrese el nombre de la comuna ","Error",JOptionPane.ERROR_MESSAGE);
            this.txtComunaB.requestFocus();
            return;
        }

        if(this.txtCarreraB.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this,"Ingrese el nombre de la sucursal ","Error",JOptionPane.ERROR_MESSAGE);
            this.txtCarreraB.requestFocus();
            return;
        }


        if(this.existeBanco() == true){
            JOptionPane.showMessageDialog(this, "Este codigo ya existe", "Error de Ingreso", JOptionPane.ERROR_MESSAGE);
        }
        else{
            insertarDatos();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnRegistrarBActionPerformed

