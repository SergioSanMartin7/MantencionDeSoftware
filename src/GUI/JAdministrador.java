    private void insertarDatos() {
        
        Administrador ban = new Administrador();
        Metodos met = new Metodos();
        
        ban.setCodigo(Integer.parseInt(this.txtRut.getText().trim()));
        ban.setNombre(this.txtNombreB.getText().trim());
        ban.setSucursal(this.txtCarreraB.getText().trim());
        ban.setDireccion(this.txtDireccionB.getText().trim());
        ban.setComuna(this.txtComunaB.getText().trim());
               
        met.ingresarBanco(ban);
               
    }
    
    private void limpiarCampos(){
        txtRut.setText("");
        txtNombreB.setText("");
        txtCarreraB.setText("");
        txtDireccionB.setText("");
        txtComunaB.setText(""); 
    }
    
    private boolean existeBanco() {
        boolean siExiste = false;
        
        Metodos met = new Metodos();
        Administrador bn = met.buscarBanco(Integer.parseInt(this.txtRut.getText().trim()));
        if(this.txtRut.getText().trim().equals(bn.getCodigo())){
            siExiste = true;
        }
        else{
            siExiste = false;
        }
        return siExiste;
    }


